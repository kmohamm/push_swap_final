#include "libft.h"

char	*ft_strcat(char *restrict s1, const char *restrict s2)
{
	int a;
	int b;

	a = 0;
	b = 0;
	while (s1[a] != '\0')
	{
		a++;
	}
	while (s2[b] != '\0')
	{
		s1[a + b] = s2[b];
		b++;
	}
	s1[a + b] = '\0';
	return (s1);
}
